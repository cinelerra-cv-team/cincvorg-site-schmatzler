    <h1>About Cinelerra</h1>
    <h2>Why three versions of Cinelerra?</h2> 
</div>

<p>Cinelerra is one of the most advanced, open-source non-linear video editors and compositors for Linux.</p>
<p>Users are frequently confused about the fact that three separately maintained versions of this software exist.</p>
<p><a href="http://lists.cinelerra-cv.org/pipermail/cinelerra/attachments/20160330/23d34507/attachment.pdf">A detailed feature comparison</a> between Cinelerra-CV, Cinelerra-HV and Cinelerra 5.1 has been posted on our mailing list on March 30, 2016. Thanks to Phyllis Smith.</p>
<p><b>We are not affiliated with cinelerra.org or Cinelerra-HV.</b> If you're interested - here's why:</p>
<br />

<h2>About Cinelerra-HV</h2>

<p>Cinelerra was originally developed by Adam Williams (formerly known as Jack Crossfire) at <a href="http://www.heroinewarrior.com/index.php">Heroine Virtual Ltd.</a> This is what the HV stands for.</p>
<p>Unlike most of the free Open Source projects, the development of Cinelerra-HV isn't open to distributed collaboration and there is no support for the software. Though in the past, Adam Williams stopped by at our mailing list occasionally.</p>
<p>Cinelerra-HV exists even today. Every few months a release is made on <a href="http://sourceforge.net/projects/heroines/">SourceForge</a>.</p>
<br />

<h2>About cinelerra.org and "5.0/5.1"</h2>
<p>Around March 2014 we overlooked the renewal of <a href="http://cinelerra.org">cinelerra.org</a>. The domain was then taken over by a different project, which is following commercial interests.</p>
<p>For a while, they released their own version ("5.0") and made their own adjustments to the software.</p>
<p>In January 2016, the developer working behind cinelerra.org ("Good Guy") joined us and continues to improve the software here. He is also accepting feedback on our mailing list.</p>
<p>This version started out as Cinelerra 4.6-mod a few years back and was renamed to 5.0 after Adam Williams showed no interest in merging its features into Cinelerra-HV.</p>
<p>The current version of this software is <b>Cinelerra 5.1 Beta</b> and <a href="http://lists.cinelerra-cv.org/pipermail/cinelerra/2016q1/004236.html">can be found here</a>.</p>
<p style="font-size:small;">You can also see a <a href="http://lists.cinelerra-cv.org/pipermail/cinelerra/attachments/20160321/bb92690e/attachment.pdf">A Brief History of Time</a>, written from the developer's point of view.</p>
<br/>

<h2>About Cinelerra-CV</h2>

<p><b>CV</b> stands for <b>C</b>ommunity <b>V</b>ersion. This website was originally meant to be a community for Cinelerra-HV, where users could come together to get help or work on improving the original software.</p>
<p>Our goals have not changed to this day:</p>
<ul>
<li>Cinelerra-CV can be considered a fork, although we try to develop it in a direction not diverging from the original and merge with all the new releases from the original project. This does <u>not</u> mean that enhancements to the software are forbidden, they are very welcome.</li>
<li>We apply bug fixes, compiler compliance fixes, and enhancements for the HV code to our <a href="https://git.cinelerra-cv.org/gitweb">git repository</a>.</li>
<li>When there is a new official release, few members (usually just Johannes Sixt) merge HV's code with our code taking the new enhancements from HV, and re-arranging our version to be more similar to the original (whitespace, function naming, directory naming, slight changes in implementations etc). During recent years, we've fallen a bit behind with merging HV's releases, but it still is our goal to do so.</li>
<li>New HV releases often have problems in the form of bugs or usability issues, since HV only builds for few distros. We intend to fix them (as users find bugs and as time permits to fix them) before making a new release.</li>
<li>Cinelerra-CV primarily focusses on stability of the software. Rather than implementing the latest cutting-edge technology, new features of the software are tested for a while and patches from the community have to be a approved by a core developer.</li>
</ul>
